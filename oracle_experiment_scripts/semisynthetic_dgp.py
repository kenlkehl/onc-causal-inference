def generate_random_equations(
    config: SemiSyntheticDGPConfig,
    confounders: List[Dict[str, Any]],
    summary_stats: Dict[str, Any],
    characteristics: List[Dict[str, Any]],
    dgp_index: int,
) -> Tuple[Dict[str, Any], Dict[str, Any]]:
    """Generate random regression equations using LLM, then rescale and calibrate.

    Uses the same approach as the fully synthetic data generator, but calibrates
    against real extracted characteristics instead of Monte Carlo samples.
    """
    seed = 42 + dgp_index * 7919 + 100
    random.seed(seed)
    np.random.seed(seed)

    llm_config = LLMConfig(
        api_base_url=config.vllm_server_url,
        model_name=config.vllm_model_name,
        max_tokens=config.vllm_max_tokens,
    )
    client = LLMClient(llm_config)

    treatment_eq, outcome_eq = _generate_equations(
        client,
        config.clinical_question,
        confounders,
        config.treatment_effect_prob,
        main_coef_scale=config.main_coefficient_scale,
        interaction_coef_scale=config.interaction_coefficient_scale,
    )

    # Rescale coefficients using real extracted characteristics
    treatment_eq, outcome_eq = _rescale_for_extracted_data(
        characteristics, confounders, summary_stats,
        treatment_eq, outcome_eq,
        target_logit_std=config.target_logit_std,
    )

    # Calibrate intercepts using real extracted characteristics
    treatment_eq, outcome_eq = _calibrate_for_extracted_data(
        characteristics, confounders, summary_stats,
        treatment_eq, outcome_eq,
        target_treatment_rate=config.target_treatment_rate,
        target_control_outcome_rate=config.target_control_outcome_rate,
    )

    return treatment_eq, outcome_eq


# ---------------------------------------------------------------------------
# Equation generation: fitted mode
# ---------------------------------------------------------------------------

def fit_equations_from_real_data(
    config: SemiSyntheticDGPConfig,
    df: pd.DataFrame,
    confounders: List[Dict[str, Any]],
    specs: List[ExplicitConfounderSpec],
    summary_stats: Dict[str, Any],
    characteristics: List[Dict[str, Any]],
) -> Tuple[Dict[str, Any], Dict[str, Any]]:
    """Fit logistic regression equations to real treatment and outcome data.

    Produces equation dicts compatible with _compute_logit() so that true ITE
    can be computed analytically.
    """
    from sklearn.linear_model import LogisticRegression

    # Build feature matrix matching _compute_logit() conventions
    X_base, base_feature_names = _build_feature_matrix(
        characteristics, confounders, summary_stats
    )

    # Build pairwise interaction features
    X_interactions, interaction_feature_names = _build_pairwise_interactions(
        X_base, base_feature_names
    )

    T = df['treatment_indicator'].values.astype(float)
    Y = df['outcome_indicator'].values.astype(float)

    # === Treatment model ===
    # logit(P(T=1|X)) = intercept + X_base @ beta + X_interactions @ gamma
    X_treat = np.hstack([X_base, X_interactions])
    penalty = config.fitted_regularization
    if penalty == "elasticnet":
        penalty = "elasticnet"
        solver = "saga"
        l1_ratio = 0.5
    else:
        solver = "lbfgs"
        l1_ratio = None

    treatment_model = LogisticRegression(
        penalty=penalty,
        C=1.0 / config.fitted_regularization_strength,
        max_iter=1000,
        solver=solver if penalty != "l1" else "saga",
        l1_ratio=l1_ratio,
    )
    treatment_model.fit(X_treat, T)
    treatment_eq = _sklearn_to_equation_dict(
        treatment_model, confounders, base_feature_names,
        interaction_feature_names,
    )
    logger.info(f"Fitted treatment equation: intercept={treatment_eq['intercept']:.3f}")

    # === Outcome model ===
    # logit(P(Y=1|X,T)) = intercept + X_base @ gamma + X_interactions @ delta
    #                      + alpha*T + X_base*T @ lambda
    X_treatment_interactions = X_base * T.reshape(-1, 1)
    treatment_interaction_names = [f"T*{n}" for n in base_feature_names]
    X_outcome = np.hstack([
        X_base, X_interactions, T.reshape(-1, 1), X_treatment_interactions
    ])

    outcome_model = LogisticRegression(
        penalty=penalty,
        C=1.0 / config.fitted_regularization_strength,
        max_iter=1000,
        solver=solver if penalty != "l1" else "saga",
        l1_ratio=l1_ratio,
    )
    outcome_model.fit(X_outcome, Y)
    outcome_eq = _sklearn_to_equation_dict_with_treatment(
        outcome_model, confounders, base_feature_names,
        interaction_feature_names, treatment_interaction_names,
    )
    logger.info(f"Fitted outcome equation: intercept={outcome_eq['intercept']:.3f}, "
                f"treatment_coefficient={outcome_eq['treatment_coefficient']:.3f}")

    return treatment_eq, outcome_eq


# ---------------------------------------------------------------------------
# Feature matrix construction (for fitted mode)
# ---------------------------------------------------------------------------

def _build_feature_matrix(
    characteristics: List[Dict[str, Any]],
    confounders: List[Dict[str, Any]],
    summary_stats: Dict[str, Any],
) -> Tuple[np.ndarray, List[str]]:
    """Build feature matrix matching _compute_logit() z-scoring conventions.

    Returns:
        (X, feature_names) where X is (N, n_features) and feature_names maps
        columns to coefficient names in the equation dict.
    """
    feature_names = []
    for conf in confounders:
        name = conf["name"]
        if conf["type"] == "continuous":
            feature_names.append(name)
        else:
            for cat in conf["categories"][1:]:  # Skip reference category
                feature_names.append(f"{name}_{cat}")

    X = np.zeros((len(characteristics), len(feature_names)), dtype=np.float64)

    for i, chars in enumerate(characteristics):
        col = 0
        for conf in confounders:
            name = conf["name"]
            if conf["type"] == "continuous":
                stats = summary_stats.get(name, {})
                mean = stats.get("mean", 0.0)
                std = stats.get("std", 1.0)
                if std == 0:
                    std = 1.0
                X[i, col] = (chars[name] - mean) / std
                col += 1
            else:
                for cat in conf["categories"][1:]:
                    X[i, col] = 1.0 if chars.get(name) == cat else 0.0
                    col += 1

    return X, feature_names


def _build_pairwise_interactions(
    X_base: np.ndarray,
    base_feature_names: List[str],
) -> Tuple[np.ndarray, List[str]]:
    """Build all pairwise interaction features."""
    n_features = X_base.shape[1]
    interaction_cols = []
    interaction_names = []

    for i in range(n_features):
        for j in range(i + 1, n_features):
            interaction_cols.append(X_base[:, i] * X_base[:, j])
            interaction_names.append(
                f"{base_feature_names[i]}*{base_feature_names[j]}"
            )

    if interaction_cols:
        X_interactions = np.column_stack(interaction_cols)
    else:
        X_interactions = np.zeros((X_base.shape[0], 0), dtype=np.float64)

    return X_interactions, interaction_names


# ---------------------------------------------------------------------------
# sklearn -> equation dict conversion
# ---------------------------------------------------------------------------

def _sklearn_to_equation_dict(
    model,
    confounders: List[Dict[str, Any]],
    base_feature_names: List[str],
    interaction_feature_names: List[str],
) -> Dict[str, Any]:
    """Convert sklearn LogisticRegression to equation dict for _compute_logit().

    The coefficient vector is [base_features..., interaction_features...].
    """
    coefs = model.coef_[0]
    intercept = float(model.intercept_[0])
    n_base = len(base_feature_names)

    # Main coefficients
    coefficients = {}
    for i, name in enumerate(base_feature_names):
        coefficients[name] = float(coefs[i])

    # Pairwise interactions
    interactions = []
    for i, name in enumerate(interaction_feature_names):
        coef_val = float(coefs[n_base + i])
        # Parse "name_i*name_j" back to terms
        parts = name.split("*", 1)
        interactions.append({
            "terms": parts,
            "coefficient": coef_val,
        })

    return {
        "intercept": intercept,
        "coefficients": coefficients,
        "interactions": interactions,
    }


def _sklearn_to_equation_dict_with_treatment(
    model,
    confounders: List[Dict[str, Any]],
    base_feature_names: List[str],
    interaction_feature_names: List[str],
    treatment_interaction_names: List[str],
) -> Dict[str, Any]:
    """Convert sklearn LogisticRegression (outcome model) to equation dict.

    The coefficient vector is:
    [base_features..., interaction_features..., T, T*base_features...]
    """
    coefs = model.coef_[0]
    intercept = float(model.intercept_[0])
    n_base = len(base_feature_names)
    n_interactions = len(interaction_feature_names)

    # Main coefficients
    coefficients = {}
    for i, name in enumerate(base_feature_names):
        coefficients[name] = float(coefs[i])

    # Pairwise confounder interactions
    interactions = []
    for i, name in enumerate(interaction_feature_names):
        coef_val = float(coefs[n_base + i])
        parts = name.split("*", 1)
        interactions.append({
            "terms": parts,
            "coefficient": coef_val,
        })

    # Treatment coefficient (T column is at index n_base + n_interactions)
    t_idx = n_base + n_interactions
    treatment_coefficient = float(coefs[t_idx])

    # Treatment-confounder interactions (T*base_features)
    treatment_interactions = []
    for i, name in enumerate(treatment_interaction_names):
        coef_val = float(coefs[t_idx + 1 + i])
        # Parse "T*feature_name" -> feature_name
        term = name.split("*", 1)[1] if "*" in name else name
        treatment_interactions.append({
            "term": term,
            "coefficient": coef_val,
        })

    return {
        "intercept": intercept,
        "coefficients": coefficients,
        "interactions": interactions,
        "treatment_coefficient": treatment_coefficient,
        "treatment_interactions": treatment_interactions,
    }


# ---------------------------------------------------------------------------
# Rescaling and calibration (adapted for extracted data)
# ---------------------------------------------------------------------------

def _sigmoid(x):
    return 1.0 / (1.0 + np.exp(-np.clip(x, -500, 500)))


def _rescale_for_extracted_data(
    characteristics: List[Dict[str, Any]],
    confounders: List[Dict[str, Any]],
    summary_stats: Dict[str, Any],
    treatment_eq: Dict[str, Any],
    outcome_eq: Dict[str, Any],
    target_logit_std: float = 2.0,
) -> Tuple[Dict[str, Any], Dict[str, Any]]:
    """Rescale coefficients to achieve target logit std using real characteristics.

    Same logic as synthetic_data.generator._rescale_for_target_logit_std but
    uses actual extracted characteristics instead of Monte Carlo samples.
    """

    def compute_logit_without_intercept(chars, equation, treatment=None):
        original = equation.get("intercept", 0.0)
        equation["intercept"] = 0.0
        logit = _compute_logit(chars, confounders, summary_stats, equation, treatment=treatment)
        equation["intercept"] = original
        return logit

    treatment_logits = np.array([
        compute_logit_without_intercept(c, treatment_eq) for c in characteristics
    ])
    outcome_logits_0 = np.array([
        compute_logit_without_intercept(c, outcome_eq, treatment=0) for c in characteristics
    ])
    outcome_logits_1 = np.array([
        compute_logit_without_intercept(c, outcome_eq, treatment=1) for c in characteristics
    ])

    treatment_std = np.std(treatment_logits)
    outcome_std = max(np.std(outcome_logits_0), np.std(outcome_logits_1))

    logger.info(f"Current logit std - treatment: {treatment_std:.2f}, outcome: {outcome_std:.2f}")

    def scale_equation(eq, scale):
        eq = eq.copy()
        if "coefficients" in eq:
            eq["coefficients"] = {k: v * scale for k, v in eq["coefficients"].items()}
        if "interactions" in eq:
            eq["interactions"] = [
                {**inter, "coefficient": inter.get("coefficient", 0) * scale}
                for inter in eq["interactions"]
            ]
        if "treatment_interactions" in eq:
            eq["treatment_interactions"] = [
                {**inter, "coefficient": inter.get("coefficient", 0) * scale}
                for inter in eq["treatment_interactions"]
            ]
        return eq

    if treatment_std > 0:
        treatment_scale = target_logit_std / treatment_std
        treatment_eq = scale_equation(treatment_eq, treatment_scale)
        logger.info(f"Scaled treatment coefficients by {treatment_scale:.3f}")

    if outcome_std > 0:
        outcome_scale = target_logit_std / outcome_std
        outcome_eq = scale_equation(outcome_eq, outcome_scale)
        logger.info(f"Scaled outcome coefficients by {outcome_scale:.3f}")

    return treatment_eq, outcome_eq


def _calibrate_for_extracted_data(
    characteristics: List[Dict[str, Any]],
    confounders: List[Dict[str, Any]],
    summary_stats: Dict[str, Any],
    treatment_eq: Dict[str, Any],
    outcome_eq: Dict[str, Any],
    target_treatment_rate: float = 0.5,
    target_control_outcome_rate: float = 0.2,
) -> Tuple[Dict[str, Any], Dict[str, Any]]:
    """Calibrate intercepts using real extracted characteristics.

    Same logic as synthetic_data.generator._calibrate_intercepts but uses
    actual characteristics instead of Monte Carlo samples.
    """

    def compute_lp(chars, equation, treatment=None):
        original = equation.get("intercept", 0.0)
        equation["intercept"] = 0.0
        lp = _compute_logit(chars, confounders, summary_stats, equation, treatment=treatment)
        equation["intercept"] = original
        return lp

    treatment_lps = np.array([compute_lp(c, treatment_eq) for c in characteristics])
    outcome_lps = np.array([compute_lp(c, outcome_eq, treatment=0) for c in characteristics])

    # Calibrate treatment intercept
    def treatment_rate_error(intercept):
        return _sigmoid(intercept + treatment_lps).mean() - target_treatment_rate

    try:
        cal_treat = brentq(treatment_rate_error, -10, 10)
    except ValueError:
        cal_treat = -10 if treatment_rate_error(-10) > 0 else 10
        logger.warning(f"Could not achieve target treatment rate {target_treatment_rate}")

    # Calibrate outcome intercept (control group)
    def outcome_rate_error(intercept):
        return _sigmoid(intercept + outcome_lps).mean() - target_control_outcome_rate

    try:
        cal_out = brentq(outcome_rate_error, -10, 10)
    except ValueError:
        cal_out = -10 if outcome_rate_error(-10) > 0 else 10
        logger.warning(f"Could not achieve target control outcome rate {target_control_outcome_rate}")

    old_t = treatment_eq.get("intercept", 0.0)
    old_o = outcome_eq.get("intercept", 0.0)
    treatment_eq["intercept"] = cal_treat
    outcome_eq["intercept"] = cal_out

    achieved_t = _sigmoid(cal_treat + treatment_lps).mean()
    achieved_o = _sigmoid(cal_out + outcome_lps).mean()
    logger.info(f"Treatment intercept: {old_t:.3f} -> {cal_treat:.3f} "
                f"(rate={achieved_t:.3f}, target={target_treatment_rate:.3f})")
    logger.info(f"Outcome intercept: {old_o:.3f} -> {cal_out:.3f} "
                f"(rate={achieved_o:.3f}, target={target_control_outcome_rate:.3f})")

    return treatment_eq, outcome_eq


