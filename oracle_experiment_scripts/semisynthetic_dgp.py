@dataclass
class SemiSyntheticDGPConfig:
    """Configuration for semi-synthetic DGP generation."""

    # Clinical context (required)
    clinical_question: str = ""
    num_confounders: int = 10

    # DGP mode: "random" or "fitted"
    equation_mode: str = "random"

    # Whether each DGP gets fresh confounders (None = mode default)
    vary_confounders_per_dgp: Optional[bool] = None

    # Random mode params
    treatment_effect_prob: float = 0.10
    target_treatment_rate: float = 0.5
    target_control_outcome_rate: float = 0.2
    main_coefficient_scale: float = 0.3
    interaction_coefficient_scale: float = 0.1
    target_logit_std: float = 2.0

    # Fitted mode params
    fitted_interaction_terms: bool = True
    fitted_regularization: str = "l2"
    fitted_regularization_strength: float = 1.0

    # vLLM settings (used for both confounder extraction and generation)
    vllm_mode: str = "server"
    vllm_server_url: str = "http://localhost:8000/v1"
    vllm_model_name: str = "openai/gpt-oss-120b"
    vllm_tensor_parallel_size: int = 1
    vllm_download_dir: Optional[str] = None
    vllm_max_model_len: int = 120000
    vllm_max_tokens: int = 5000
    extraction_batch_size: int = 32

    @property
    def should_vary_confounders(self) -> bool:
        if self.vary_confounders_per_dgp is not None:
            return self.vary_confounders_per_dgp
        return self.equation_mode == "random"


@dataclass
class SemiSyntheticDGP:
    """A generated DGP with extracted confounders and equations."""

    confounders: List[Dict[str, Any]]           # confounder dicts from LLM
    confounder_specs: List[ExplicitConfounderSpec]
    treatment_equation: Dict[str, Any]
    outcome_equation: Dict[str, Any]
    summary_stats: Dict[str, Any]               # computed from extracted values
    extracted_df: pd.DataFrame                   # explicit_conf_* columns
    characteristics: List[Dict[str, Any]]        # per-patient characteristic dicts
    dgp_index: int
    seed: int


# ---------------------------------------------------------------------------
# Parallel extraction across multiple vLLM servers
# ---------------------------------------------------------------------------

def _parallel_extract_to_dataframe(
    texts: List[str],
    specs: List[ExplicitConfounderSpec],
    server_urls: List[str],
    model_name: str,
    batch_size: int = 32,
) -> pd.DataFrame:
    """Extract confounders in parallel across multiple vLLM servers.

    Splits texts across servers using interleaving for load balancing,
    extracts in parallel using ThreadPoolExecutor (I/O-bound HTTP calls),
    and reassembles results in original order.
    """
    from concurrent.futures import ThreadPoolExecutor

    n_servers = len(server_urls)

    # Interleave texts across servers for load balancing
    chunks = [[] for _ in range(n_servers)]
    chunk_indices = [[] for _ in range(n_servers)]
    for i, text in enumerate(texts):
        server_idx = i % n_servers
        chunks[server_idx].append(text)
        chunk_indices[server_idx].append(i)

    logger.info(f"Parallel extraction: {len(texts)} texts across {n_servers} servers "
                f"({', '.join(str(len(c)) for c in chunks)} texts each)")

    def extract_chunk(args):
        chunk_texts, server_url = args
        extractor = VLLMConfounderExtractor(
            specs=specs,
            mode="server",
            server_url=server_url,
            model_name=model_name,
            max_retries=2,
            temperature=0.0,
        )
        return extractor.extract_to_dataframe(chunk_texts, batch_size=batch_size)

    with ThreadPoolExecutor(max_workers=n_servers) as executor:
        results = list(executor.map(extract_chunk, zip(chunks, server_urls)))

    # Reassemble in original order
    all_dfs = []
    for indices, chunk_df in zip(chunk_indices, results):
        chunk_df.index = indices
        all_dfs.append(chunk_df)

    merged = pd.concat(all_dfs).sort_index().reset_index(drop=True)
    return merged


# ---------------------------------------------------------------------------
# Confounder generation and extraction
# ---------------------------------------------------------------------------

def generate_and_extract_confounders(
    config: SemiSyntheticDGPConfig,
    texts: List[str],
    dataset_path: str,
    dgp_index: int,
    cache_dir: Optional[str] = None,
    server_urls: Optional[List[str]] = None,
) -> Tuple[List[Dict[str, Any]], List[ExplicitConfounderSpec], pd.DataFrame]:
    """Generate confounders via LLM and extract from real text.

    Returns:
        (confounders, specs, extracted_df)
    """
    # Seed for confounder generation reproducibility
    seed = 42 + dgp_index * 7919
    random.seed(seed)
    np.random.seed(seed)

    # If mode is start_server, create the extractor early so the server
    # is running before the LLMClient tries to connect to the same URL.
    extractor = None
    if config.vllm_mode == "start_server":
        extractor = VLLMConfounderExtractor(
            specs=[],  # placeholder, updated after confounder generation
            mode=config.vllm_mode,
            server_url=config.vllm_server_url,
            model_name=config.vllm_model_name,
            tensor_parallel_size=config.vllm_tensor_parallel_size,
            download_dir=config.vllm_download_dir,
            max_model_len=config.vllm_max_model_len,
            max_retries=2,
            temperature=0.0,
        )
        extractor._ensure_initialized()

    # Step 1: Generate confounder definitions via LLM
    llm_config = LLMConfig(
        api_base_url=config.vllm_server_url,
        model_name=config.vllm_model_name,
        max_tokens=config.vllm_max_tokens,
    )
    client = LLMClient(llm_config)
    confounders = _generate_confounders(
        client, config.clinical_question, num_confounders=config.num_confounders
    )
    logger.info(
        f"DGP {dgp_index}: Generated {len(confounders)} confounders: "
        f"{[c['name'] for c in confounders]}"
    )

    # Step 2: Convert to ExplicitConfounderSpec
    specs = []
    for conf in confounders:
        specs.append(ExplicitConfounderSpec(
            name=conf["name"],
            type=conf["type"],
            categories=conf.get("categories"),
            description=conf.get("description", conf["name"]),
        ))

    # Step 3: Check cache
    cache = ExtractionCache(cache_dir=cache_dir)
    cache_config = {
        'confounders': specs,
        'vllm_model_name': config.vllm_model_name,
        'extraction_temperature': 0.0,
        'extraction_max_tokens': 1024,
    }
    cached_df = cache.load_if_valid(
        dataset_path, cache_config, expected_rows=len(texts)
    )

    if cached_df is not None:
        logger.info(f"DGP {dgp_index}: Using cached extraction results")
        extracted_df = cached_df
        if extractor is not None:
            extractor.cleanup()
    else:
        # Step 4: Extract from real text
        if server_urls and len(server_urls) > 1:
            # Parallel extraction across multiple vLLM servers
            extracted_df = _parallel_extract_to_dataframe(
                texts, specs, server_urls,
                model_name=config.vllm_model_name,
                batch_size=config.extraction_batch_size,
            )
        else:
            if extractor is not None:
                # Reuse the already-started server, just update specs
                extractor.specs = specs
            else:
                extractor = VLLMConfounderExtractor(
                    specs=specs,
                    mode=config.vllm_mode,
                    server_url=config.vllm_server_url,
                    model_name=config.vllm_model_name,
                    tensor_parallel_size=config.vllm_tensor_parallel_size,
                    download_dir=config.vllm_download_dir,
                    max_model_len=config.vllm_max_model_len,
                    max_retries=2,
                    temperature=0.0,
                )
            extracted_df = extractor.extract_to_dataframe(
                texts, batch_size=config.extraction_batch_size
            )
            extractor.cleanup()

        # Save to cache
        cache.save(dataset_path, cache_config, extracted_df)
        logger.info(f"DGP {dgp_index}: Extracted and cached confounders")

    return confounders, specs, extracted_df


# ---------------------------------------------------------------------------
# Summary statistics from extracted values
# ---------------------------------------------------------------------------

def compute_summary_stats_from_extracted(
    extracted_df: pd.DataFrame,
    specs: List[ExplicitConfounderSpec],
) -> Dict[str, Any]:
    """Compute summary statistics from extracted confounder values.

    Unlike the synthetic data pipeline which uses LLM-generated stats,
    these are computed directly from the actual extracted values.
    """
    stats = {}
    for spec in specs:
        col = f"explicit_conf_{spec.name}"
        miss_col = f"explicit_conf_{spec.name}_missing"

        if miss_col in extracted_df.columns:
            non_missing_mask = ~extracted_df[miss_col].astype(bool)
        else:
            non_missing_mask = extracted_df[col].notna()

        non_missing = extracted_df.loc[non_missing_mask, col]

        if spec.type == "continuous":
            values = pd.to_numeric(non_missing, errors='coerce').dropna()
            if len(values) > 0:
                stats[spec.name] = {
                    "mean": float(values.mean()),
                    "std": float(max(values.std(), 1e-6)),
                }
            else:
                stats[spec.name] = {"mean": 0.0, "std": 1.0}
        else:
            # Categorical: compute proportions
            if len(non_missing) > 0:
                counts = non_missing.value_counts(normalize=True)
                proportions = {}
                for cat in (spec.categories or []):
                    proportions[cat] = float(counts.get(cat, 0.0))
                # Normalize in case some categories weren't extracted
                total = sum(proportions.values())
                if total > 0:
                    proportions = {k: v / total for k, v in proportions.items()}
                stats[spec.name] = {"proportions": proportions}
            else:
                n_cats = len(spec.categories or ["0", "1"])
                stats[spec.name] = {
                    "proportions": {
                        cat: 1.0 / n_cats for cat in (spec.categories or ["0", "1"])
                    }
                }

    return stats


# ---------------------------------------------------------------------------
# Build patient characteristics from extracted data
# ---------------------------------------------------------------------------

def build_characteristics_from_extracted(
    extracted_df: pd.DataFrame,
    specs: List[ExplicitConfounderSpec],
    summary_stats: Dict[str, Any],
) -> List[Dict[str, Any]]:
    """Convert extracted DataFrame to list of characteristic dicts.

    Imputes missing values (median for continuous, mode for categorical)
    so every patient has complete characteristics for _compute_logit().
    """
    # Compute imputation values
    imputation = {}
    for spec in specs:
        col = f"explicit_conf_{spec.name}"
        miss_col = f"explicit_conf_{spec.name}_missing"

        if miss_col in extracted_df.columns:
            non_missing_mask = ~extracted_df[miss_col].astype(bool)
        else:
            non_missing_mask = extracted_df[col].notna()

        non_missing = extracted_df.loc[non_missing_mask, col]

        if spec.type == "continuous":
            values = pd.to_numeric(non_missing, errors='coerce').dropna()
            imputation[spec.name] = float(values.median()) if len(values) > 0 else 0.0
        else:
            if len(non_missing) > 0:
                imputation[spec.name] = non_missing.mode().iloc[0]
            elif spec.categories:
                imputation[spec.name] = spec.categories[0]
            else:
                imputation[spec.name] = "0"

    # Build per-patient characteristics
    characteristics = []
    for idx in range(len(extracted_df)):
        chars = {}
        for spec in specs:
            col = f"explicit_conf_{spec.name}"
            miss_col = f"explicit_conf_{spec.name}_missing"

            is_missing = False
            if miss_col in extracted_df.columns:
                is_missing = bool(extracted_df.iloc[idx][miss_col])
            else:
                is_missing = pd.isna(extracted_df.iloc[idx][col])

            if is_missing:
                chars[spec.name] = imputation[spec.name]
            else:
                val = extracted_df.iloc[idx][col]
                if spec.type == "continuous":
                    try:
                        chars[spec.name] = float(val)
                    except (ValueError, TypeError):
                        chars[spec.name] = imputation[spec.name]
                else:
                    chars[spec.name] = str(val)

        characteristics.append(chars)

    return characteristics


