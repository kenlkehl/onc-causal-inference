def generate_dgp(
    config: SemiSyntheticDGPConfig,
    texts: List[str],
    df: pd.DataFrame,
    dataset_path: str,
    dgp_index: int,
    cache_dir: Optional[str] = None,
    confounders: Optional[List[Dict[str, Any]]] = None,
    specs: Optional[List[ExplicitConfounderSpec]] = None,
    extracted_df: Optional[pd.DataFrame] = None,
    server_urls: Optional[List[str]] = None,
) -> SemiSyntheticDGP:
    """Generate a complete semi-synthetic DGP.

    When confounders/specs/extracted_df are provided (vary_confounders=False),
    reuses them instead of generating new ones.

    Args:
        config: DGP configuration
        texts: List of clinical text strings
        df: Real dataset (needs treatment_indicator, outcome_indicator for fitted mode)
        dataset_path: Path to dataset (for cache keying)
        dgp_index: Index of this DGP (used for seeding)
        cache_dir: Optional cache directory for extraction results
        confounders: Pre-generated confounders (for vary_confounders=False)
        specs: Pre-generated specs (for vary_confounders=False)
        extracted_df: Pre-extracted DataFrame (for vary_confounders=False)
    """
    seed = 42 + dgp_index * 7919
    random.seed(seed)
    np.random.seed(seed)

    # Step 1: Generate/reuse confounders and extract from text
    if confounders is None or specs is None or extracted_df is None:
        confounders, specs, extracted_df = generate_and_extract_confounders(
            config, texts, dataset_path, dgp_index, cache_dir=cache_dir,
            server_urls=server_urls,
        )

    # Step 2: Compute summary statistics from extracted values
    summary_stats = compute_summary_stats_from_extracted(extracted_df, specs)

    # Step 3: Build patient characteristics (with imputation)
    characteristics = build_characteristics_from_extracted(
        extracted_df, specs, summary_stats
    )

    # Step 4: Generate equations
    if config.equation_mode == "random":
        treatment_eq, outcome_eq = generate_random_equations(
            config, confounders, summary_stats, characteristics, dgp_index,
        )
    elif config.equation_mode == "fitted":
        treatment_eq, outcome_eq = fit_equations_from_real_data(
            config, df, confounders, specs, summary_stats, characteristics,
        )
    else:
        raise ValueError(f"Unknown equation_mode: {config.equation_mode}")

    # Log extraction quality
    n_patients = len(extracted_df)
    total_missing = 0
    for spec in specs:
        miss_col = f"explicit_conf_{spec.name}_missing"
        if miss_col in extracted_df.columns:
            total_missing += extracted_df[miss_col].sum()
    missingness_rate = total_missing / (n_patients * len(specs)) if specs else 0
    logger.info(f"DGP {dgp_index}: extraction missingness rate = {missingness_rate:.3f}")

    return SemiSyntheticDGP(
        confounders=confounders,
        confounder_specs=specs,
        treatment_equation=treatment_eq,
        outcome_equation=outcome_eq,
        summary_stats=summary_stats,
        extracted_df=extracted_df,
        characteristics=characteristics,
        dgp_index=dgp_index,
        seed=seed,
    )


def select_confounder_subset(
    specs: List[ExplicitConfounderSpec],
    n_subset: int,
    seed: int,
) -> List[ExplicitConfounderSpec]:
    """Select a random subset of confounders.

    Returns all specs when n_subset >= len(specs), empty list when n_subset <= 0.
    """
    if n_subset <= 0:
        return []
    if n_subset >= len(specs):
        return list(specs)
    rng = np.random.RandomState(seed)
    indices = rng.choice(len(specs), size=n_subset, replace=False)
    indices.sort()  # Preserve relative ordering for reproducibility
    return [specs[i] for i in indices]


def save_dgp_metadata(dgp: SemiSyntheticDGP, output_dir: Path, dgp_index: int):
    """Save DGP metadata to JSON."""
    metadata_dir = output_dir / "dgp_metadata"
    metadata_dir.mkdir(parents=True, exist_ok=True)

    # Compute extraction quality stats
    n_patients = len(dgp.extracted_df)
    extraction_stats = {}
    for spec in dgp.confounder_specs:
        miss_col = f"explicit_conf_{spec.name}_missing"
        if miss_col in dgp.extracted_df.columns:
            extraction_stats[spec.name] = {
                "missingness_rate": float(dgp.extracted_df[miss_col].mean()),
            }

    metadata = {
        "dgp_index": dgp_index,
        "seed": dgp.seed,
        "n_patients": n_patients,
        "n_confounders": len(dgp.confounder_specs),
        "confounders": dgp.confounders,
        "treatment_equation": dgp.treatment_equation,
        "outcome_equation": dgp.outcome_equation,
        "summary_stats": dgp.summary_stats,
        "extraction_stats": extraction_stats,
    }

    path = metadata_dir / f"dgp_{dgp_index}.json"
    with open(path, "w") as f:
        json.dump(metadata, f, indent=2, default=str)
    logger.info(f"Saved DGP metadata to {path}")
