def simulate_outcomes(
    characteristics: List[Dict[str, Any]],
    confounders: List[Dict[str, Any]],
    summary_stats: Dict[str, Any],
    treatment_eq: Dict[str, Any],
    outcome_eq: Dict[str, Any],
    seed: int = 42,
) -> pd.DataFrame:
    """Simulate treatment assignment, outcomes, and true ITE for each patient.

    Returns DataFrame with columns:
        treatment_indicator, outcome_indicator,
        true_treatment_prob, true_outcome_prob,
        true_y0_prob, true_y1_prob, true_ite_prob
    """
    rng = np.random.RandomState(seed)
    n = len(characteristics)

    treatment_probs = np.zeros(n)
    y0_probs = np.zeros(n)
    y1_probs = np.zeros(n)

    for i, chars in enumerate(characteristics):
        # Treatment probability
        t_logit = _compute_logit(chars, confounders, summary_stats, treatment_eq)
        treatment_probs[i] = _sigmoid(t_logit)

        # Potential outcomes
        y0_logit = _compute_logit(chars, confounders, summary_stats, outcome_eq, treatment=0)
        y1_logit = _compute_logit(chars, confounders, summary_stats, outcome_eq, treatment=1)
        y0_probs[i] = _sigmoid(y0_logit)
        y1_probs[i] = _sigmoid(y1_logit)

    # Sample treatment and outcome
    treatments = rng.binomial(1, treatment_probs)
    outcome_probs = np.where(treatments == 1, y1_probs, y0_probs)
    outcomes = rng.binomial(1, outcome_probs)

    true_ite = y1_probs - y0_probs

    logger.info(
        f"Simulated outcomes: treatment_rate={treatments.mean():.3f}, "
        f"outcome_rate={outcomes.mean():.3f}, "
        f"mean_ITE={true_ite.mean():.4f}, std_ITE={true_ite.std():.4f}"
    )

    return pd.DataFrame({
        'treatment_indicator': treatments,
        'outcome_indicator': outcomes,
        'true_treatment_prob': treatment_probs,
        'true_outcome_prob': outcome_probs,
        'true_y0_prob': y0_probs,
        'true_y1_prob': y1_probs,
        'true_ite_prob': true_ite,
    })


