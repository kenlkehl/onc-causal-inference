# oracle_experiment_scripts/semisynthetic_dgp.py
"""Semi-synthetic data generating process (DGP) for sensitivity analysis.

Uses real clinical text but simulated treatments/outcomes with known true ITEs.
Confounders are generated by an LLM and extracted from real text via vLLM, then
regression equations (random or fitted to real data) produce simulated T/Y.

Two equation modes:
- "random": LLM generates regression equations with random coefficients + calibration
- "fitted": Logistic regression fit on extracted confounders to predict real T and Y

Reuses:
- synthetic_data.generator: _generate_confounders, _generate_equations, _compute_logit
- oci.extraction: VLLMConfounderExtractor, ExtractionCache
"""

