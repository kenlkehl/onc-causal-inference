def convert_structured_event_to_text(event_type: str, event_text: str) -> Optional[str]:
    """Convert a structured event to standardized text.

    Args:
        event_type: One of the STRUCTURED_EVENT_TYPES
        event_text: Raw event text from the timeline

    Returns:
        Formatted text string, or None if parsing fails
    """
    try:
        event_text = _normalize_hyphens(event_text)
        if event_type == "encounter":
            parsed = parse_encounter_event(event_text)
            return encounter_to_text(parsed)
        elif event_type == "hospitalization":
            parsed = parse_hospitalization_event(event_text)
            return hospitalization_to_text(parsed)
        elif event_type == "lab_result":
            parsed = parse_lab_result_event(event_text)
            return lab_result_to_text(parsed)
        elif event_type == "pro_assessment":
            parsed = parse_pro_assessment_event(event_text)
            return pro_assessment_to_text(parsed)
        else:
            logger.warning(f"Unknown structured event type: {event_type}")
            return None
    except Exception as e:
        logger.warning(f"Failed to parse structured event ({event_type}): {e}")
        return None
