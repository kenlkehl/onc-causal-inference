def parse_encounter_event(event_text: str) -> Dict[str, Any]:
    """Parse an encounter event from the timeline into structured fields.

    Expected format:
        At age 62, outpatient oncology visit. DX: C34.90 (description), Z51.11 (description).
        CPT: 99214 (description), 96413 (description).
    """
    result = {
        "timepoint": "",
        "encounter_type": "",
        "dx_codes": [],  # list of (code, description)
        "cpt_codes": [],  # list of (code, description)
    }

    # Extract timepoint (e.g., "At age 62")
    tp_match = re.match(r'(At age \d+[^,]*),?\s*', event_text, re.IGNORECASE)
    if tp_match:
        result["timepoint"] = tp_match.group(1).strip()
        rest = event_text[tp_match.end():]
    else:
        rest = event_text

    # Extract encounter type (text before DX: or CPT:)
    enc_match = re.match(r'(.*?)(?:\.\s*DX:|DX:|\.\s*CPT:|CPT:|$)', rest, re.IGNORECASE)
    if enc_match:
        result["encounter_type"] = enc_match.group(1).strip().rstrip('.')
        # Truncate qualifying phrases to match structured EHR format
        result["encounter_type"] = re.sub(
            r'\s+(?:for|to|regarding|focusing on|about)\s+.*$',
            '',
            result["encounter_type"],
            flags=re.IGNORECASE,
        )

    # Extract diagnosis codes: pattern like C34.90 (description) or D70.1 (description)
    dx_section_match = re.search(r'DX:\s*(.*?)(?:\.\s*CPT:|CPT:|$)', rest, re.IGNORECASE | re.DOTALL)
    if dx_section_match:
        dx_section = dx_section_match.group(1)
        # Match codes like C34.90, Z51.11, D70.1, etc.
        dx_codes = re.findall(
            r'([A-Z]\d{2}(?:\.\d{1,4})?)\s*\(([^)]+)\)',
            dx_section,
            re.IGNORECASE
        )
        result["dx_codes"] = [(code.upper(), desc.strip()) for code, desc in dx_codes]

    # Extract CPT/HCPCS codes: pattern like 99214 (description)
    cpt_section_match = re.search(r'CPT:\s*(.*?)$', rest, re.IGNORECASE | re.DOTALL)
    if cpt_section_match:
        cpt_section = cpt_section_match.group(1)
        cpt_codes = re.findall(
            r'(\d{5})\s*\(([^)]+)\)',
            cpt_section
        )
        result["cpt_codes"] = [(code, desc.strip()) for code, desc in cpt_codes]

    return result


def parse_hospitalization_event(event_text: str) -> Dict[str, Any]:
    """Parse a hospitalization event from the timeline into structured fields.

    Expected format:
        At age 63, admitted for febrile neutropenia. Principal DX: D70.1
        (Agranulocytosis secondary to cancer chemotherapy). LOS: 4 days.
        Discharge: home.
    """
    result = {
        "timepoint": "",
        "reason": "",
        "principal_dx_code": "",
        "principal_dx_desc": "",
        "los_days": "",
        "discharge_disposition": "",
    }

    # Extract timepoint
    tp_match = re.match(r'(At age \d+[^,]*),?\s*', event_text, re.IGNORECASE)
    if tp_match:
        result["timepoint"] = tp_match.group(1).strip()
        rest = event_text[tp_match.end():]
    else:
        rest = event_text

    # Extract admission reason
    reason_match = re.match(r'admitted (?:for|with|due to)\s+(.*?)(?:\.\s*Principal|Principal|\.\s*DX|DX|$)',
                            rest, re.IGNORECASE)
    if reason_match:
        result["reason"] = reason_match.group(1).strip().rstrip('.')

    # Extract principal diagnosis code
    dx_match = re.search(
        r'(?:Principal\s+)?DX:\s*([A-Z]\d{2}(?:\.\d{1,4})?)\s*\(([^)]+)\)',
        rest,
        re.IGNORECASE
    )
    if dx_match:
        result["principal_dx_code"] = dx_match.group(1).upper()
        result["principal_dx_desc"] = dx_match.group(2).strip()

    # Extract LOS
    los_match = re.search(r'LOS:\s*(\d+)\s*days?', rest, re.IGNORECASE)
    if los_match:
        result["los_days"] = los_match.group(1)

    # Extract discharge disposition
    discharge_match = re.search(r'Discharge:\s*(.+?)(?:\.|$)', rest, re.IGNORECASE)
    if discharge_match:
        result["discharge_disposition"] = discharge_match.group(1).strip()

    return result


def parse_lab_result_event(event_text: str) -> Dict[str, Any]:
    """Parse a lab result event from the timeline into structured fields.

    Expected format:
        At age 62, CBC: WBC 8.2 k/uL (normal), Hgb 11.5 g/dL (low), Plt 245 k/uL (normal).
        CMP: Cr 0.9 mg/dL (normal), AST 28 U/L (normal).
    """
    result = {
        "timepoint": "",
        "panels": [],  # list of {"name": str, "components": [{"name", "value", "unit", "flag"}]}
    }

    # Extract timepoint
    tp_match = re.match(r'(At age \d+[^,]*),?\s*', event_text, re.IGNORECASE)
    if tp_match:
        result["timepoint"] = tp_match.group(1).strip()
        rest = event_text[tp_match.end():]
    else:
        rest = event_text

    # Split into panels by known panel names
    panel_pattern = re.compile(
        r'(CBC|CMP|LFT|BMP|Tumor [Mm]arkers?|Coagulation|Hepatic [Pp]anel|Renal [Pp]anel):\s*',
        re.IGNORECASE
    )

    # Find all panel headers and their positions
    panel_matches = list(panel_pattern.finditer(rest))

    if panel_matches:
        for i, match in enumerate(panel_matches):
            panel_name = match.group(1)
            start = match.end()
            end = panel_matches[i + 1].start() if i + 1 < len(panel_matches) else len(rest)
            panel_text = rest[start:end].strip().rstrip('.')

            components = _parse_lab_components(panel_text)
            result["panels"].append({
                "name": _normalize_panel_name(panel_name),
                "components": components,
            })
    else:
        # No panel headers found - try to parse as a flat list of components
        components = _parse_lab_components(rest)
        if components:
            result["panels"].append({
                "name": "Laboratory Results",
                "components": components,
            })

    return result


def _parse_lab_components(text: str) -> List[Dict[str, str]]:
    """Parse individual lab components from text.

    Matches patterns like:
        WBC 8.2 k/uL (normal)
        Hgb 11.5 g/dL (low)
        Cr 0.9 mg/dL (normal)
        CEA 5.2 ng/mL (high)
    """
    components = []
    # Match: name value unit (flag)
    # Name can be multi-word (e.g., "CA 19-9", "Total Bilirubin", "Alkaline Phosphatase")
    pattern = re.compile(
        r'([A-Za-z][A-Za-z0-9 .\-]*?)\s+'   # name (non-greedy)
        r'([\d.]+)\s*'                        # value
        r'([A-Za-z/%]+(?:/[A-Za-z]+)?)\s*'   # unit
        r'\((\w+)\)',                          # flag in parens
        re.IGNORECASE
    )
    for match in pattern.finditer(text):
        name = match.group(1).strip()
        value = match.group(2)
        unit = match.group(3)
        flag = match.group(4).strip().lower()
        components.append({
            "name": name,
            "value": value,
            "unit": unit,
            "flag": flag,
        })
    return components


def _normalize_panel_name(name: str) -> str:
    """Normalize panel name to display form."""
    mapping = {
        "cbc": "Complete Blood Count (CBC)",
        "cmp": "Comprehensive Metabolic Panel (CMP)",
        "bmp": "Basic Metabolic Panel (BMP)",
        "lft": "Liver Function Tests (LFTs)",
        "hepatic panel": "Hepatic Panel",
        "renal panel": "Renal Panel",
        "coagulation": "Coagulation Panel",
    }
    lower = name.lower().strip()
    if lower in mapping:
        return mapping[lower]
    if lower.startswith("tumor marker"):
        return "Tumor Markers"
    return name


def parse_pro_assessment_event(event_text: str) -> Dict[str, Any]:
    """Parse a PRO assessment event from the timeline into structured fields.

    Expected format:
        At age 63, EORTC QLQ-C30: Physical Function 60, Role Function 40, ...
        PRO-CTCAE: Nausea severity 2, Fatigue severity 3, ...
    """
    result = {
        "timepoint": "",
        "instruments": [],  # list of {"name": str, "scores": [{"subscale", "value"}]}
    }

    # Extract timepoint
    tp_match = re.match(r'(At age \d+[^,]*),?\s*', event_text, re.IGNORECASE)
    if tp_match:
        result["timepoint"] = tp_match.group(1).strip()
        rest = event_text[tp_match.end():]
    else:
        rest = event_text

    # Split by instrument names
    instrument_pattern = re.compile(
        r'(EORTC QLQ-?C30|PRO-?CTCAE|FACT-?G|EQ-?5D-?5L|PROMIS-?29)[^:]*:\s*',
        re.IGNORECASE
    )
    inst_matches = list(instrument_pattern.finditer(rest))

    for i, match in enumerate(inst_matches):
        inst_name = match.group(1).upper().replace(" ", "-")
        start = match.end()
        end = inst_matches[i + 1].start() if i + 1 < len(inst_matches) else len(rest)
        inst_text = rest[start:end].strip().rstrip('.')

        scores = _parse_pro_scores(inst_text, inst_name)
        result["instruments"].append({
            "name": _normalize_instrument_name(inst_name),
            "scores": scores,
        })

    return result


def _parse_pro_scores(text: str, instrument_name: str) -> List[Dict[str, str]]:
    """Parse PRO subscale scores from text."""
    scores = []

    if "CTCAE" in instrument_name.upper():
        # PRO-CTCAE format: "Symptom severity N" or "Symptom N"
        pattern = re.compile(
            r'([A-Za-z][A-Za-z ]+?)\s+'
            r'(?:severity\s+)?(\d)',
            re.IGNORECASE
        )
        for match in pattern.finditer(text):
            subscale = match.group(1).strip()
            value = match.group(2)
            # Skip if the subscale name is just "severity" or similar noise
            if subscale.lower() in ("severity", "frequency", "interference"):
                continue
            scores.append({"subscale": subscale, "value": value})
    else:
        # EORTC QLQ-C30 format: "Subscale Name VALUE" (0-100 scale)
        pattern = re.compile(
            r'([A-Za-z][A-Za-z ]+?)\s+(\d{1,3})(?:\s*[,.]|\s*$|\s+(?=[A-Z]))',
            re.IGNORECASE
        )
        for match in pattern.finditer(text):
            subscale = match.group(1).strip()
            value = match.group(2)
            scores.append({"subscale": subscale, "value": value})

    return scores


def _normalize_instrument_name(name: str) -> str:
    """Normalize PRO instrument name to display form."""
    upper = name.upper().replace(" ", "-")
    if "QLQ" in upper or "C30" in upper:
        return "EORTC QLQ-C30"
    if "CTCAE" in upper:
        return "PRO-CTCAE"
    if "FACT" in upper:
        return "FACT-G"
    if "EQ" in upper and "5D" in upper:
        return "EQ-5D-5L"
    if "PROMIS" in upper:
        return "PROMIS-29"
    return name


