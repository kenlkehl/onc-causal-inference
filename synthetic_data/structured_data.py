def encounter_to_text(parsed: Dict[str, Any]) -> str:
    """Convert parsed encounter event to standardized text."""
    lines = ["Encounter Record"]

    if parsed.get("timepoint"):
        lines.append(f"  Timepoint: {parsed['timepoint']}")
    if parsed.get("encounter_type"):
        lines.append(f"  Encounter Type: {parsed['encounter_type']}")

    if parsed.get("dx_codes"):
        lines.append("  Diagnosis Codes:")
        for code, desc in parsed["dx_codes"]:
            lines.append(f"    - {code}: {desc}")

    if parsed.get("cpt_codes"):
        lines.append("  Procedure Codes:")
        for code, desc in parsed["cpt_codes"]:
            lines.append(f"    - {code}: {desc}")

    return "\n".join(lines)


def hospitalization_to_text(parsed: Dict[str, Any]) -> str:
    """Convert parsed hospitalization event to standardized text."""
    lines = ["Hospital Admission Record"]

    if parsed.get("timepoint"):
        lines.append(f"  Timepoint: {parsed['timepoint']}")
    if parsed.get("reason"):
        lines.append(f"  Reason for Admission: {parsed['reason']}")
    if parsed.get("principal_dx_code"):
        lines.append(f"  Principal Diagnosis: {parsed['principal_dx_code']} - {parsed.get('principal_dx_desc', '')}")
    if parsed.get("los_days"):
        lines.append(f"  Length of Stay: {parsed['los_days']} days")
    if parsed.get("discharge_disposition"):
        lines.append(f"  Discharge Disposition: {parsed['discharge_disposition']}")

    return "\n".join(lines)


def lab_result_to_text(parsed: Dict[str, Any]) -> str:
    """Convert parsed lab result event to standardized text."""
    lines = ["Laboratory Results"]

    if parsed.get("timepoint"):
        lines.append(f"  Timepoint: {parsed['timepoint']}")

    for panel in parsed.get("panels", []):
        lines.append(f"  {panel['name']}:")
        for comp in panel.get("components", []):
            name = comp["name"]
            value = comp["value"]
            unit = comp.get("unit", "")
            flag = comp.get("flag", "normal")

            # Look up reference range
            ref_range = _get_reference_range(name)
            if ref_range:
                ref_unit, ref_low, ref_high = ref_range
                flag_label = flag.upper() if flag.lower() not in ("normal", "wnl") else "Normal"
                lines.append(
                    f"    {name}: {value} {unit} "
                    f"[Reference: {ref_low}-{ref_high}] - {flag_label}"
                )
            else:
                flag_label = flag.upper() if flag.lower() not in ("normal", "wnl") else "Normal"
                lines.append(f"    {name}: {value} {unit} - {flag_label}")

    return "\n".join(lines)


def _get_reference_range(lab_name: str) -> Optional[Tuple[str, float, float]]:
    """Look up reference range for a lab component by name."""
    lower = lab_name.lower().strip()
    if lower in LAB_REFERENCE_RANGES:
        return LAB_REFERENCE_RANGES[lower]
    # Try common abbreviation mappings
    abbrev_map = {
        "white blood cell": "wbc",
        "white blood cells": "wbc",
        "red blood cells": "rbc",
        "hemoglobin": "hemoglobin",
        "hematocrit": "hematocrit",
        "platelet": "plt",
        "platelets": "plt",
        "absolute neutrophil count": "anc",
        "creatinine": "creatinine",
        "blood urea nitrogen": "bun",
        "alanine aminotransferase": "alt",
        "aspartate aminotransferase": "ast",
    }
    if lower in abbrev_map:
        key = abbrev_map[lower]
        return LAB_REFERENCE_RANGES.get(key)
    return None


def pro_assessment_to_text(parsed: Dict[str, Any]) -> str:
    """Convert parsed PRO assessment event to standardized text."""
    lines = ["Patient-Reported Outcomes Assessment"]

    if parsed.get("timepoint"):
        lines.append(f"  Timepoint: {parsed['timepoint']}")

    for instrument in parsed.get("instruments", []):
        inst_name = instrument["name"]
        lines.append(f"  Instrument: {inst_name}")

        for score in instrument.get("scores", []):
            subscale = score["subscale"]
            value = score["value"]

            if "CTCAE" in inst_name.upper():
                # PRO-CTCAE: 0-4 scale with severity labels
                try:
                    severity_int = int(value)
                    label = PRO_CTCAE_SEVERITY_LABELS.get(severity_int, "")
                    lines.append(f"    {subscale} - Severity: {value}/4 ({label})")
                except ValueError:
                    lines.append(f"    {subscale} - Severity: {value}/4")
            else:
                # EORTC QLQ-C30: 0-100 scale
                lines.append(f"    {subscale}: {value}/100")

    return "\n".join(lines)


